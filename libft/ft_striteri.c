#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t	x;

	if ((s == NULL) || (f == NULL))
		return ;
	x = 0;
	while (s[x] != '\0')
	{
		f(x, s + x);
		x++;
	}
}
