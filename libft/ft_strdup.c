#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dst;
	int		i;

	i = 0;
	i = ft_strlen((char *)s);
	dst = (char *)malloc(sizeof(*dst) * (i + 1));
	i = 0;
	if (!dst)
		return (NULL);
	while (s[i])
	{
		dst[i] = s[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
